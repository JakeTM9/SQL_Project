USE [School]

SELECT S.[STD_CODE]
    ,[STD_FNAME]
    ,[STD_LNAME]
    ,[STD_CUM_GPA]
    ,[STD_START_DATE]
	,[ENR_CODE]
	,[ENR_GRADE]
	,[ENR_START_DATE]
	,[ENR_END_DATE]
	,[CRS_CODE]
FROM [dbo].[Student] S INNER JOIN [dbo].[Enrollment] E
ON S.[STD_CODE] = E.[STD_CODE]
ORDER BY S.[STD_CODE]

SELECT [ENR_CODE]
	,[ENR_GRADE]
	,[ENR_START_DATE]
	,[ENR_END_DATE]
	,E.[CRS_CODE]
	,[STD_CODE]
	,C.[CRS_CODE]
	,[CRS_TITLE]
	,[CRS_CREDIT_HOURS]
	,[CRS_DESCRIPTION]
	,[CRS_BOOK_NAME]
	,[INS_CODE]
	,[DPT_CODE]
FROM [dbo].[Enrollment] E INNER JOIN [dbo].[Course] C
ON E.[CRS_CODE] = C.[CRS_CODE]
ORDER BY E.[ENR_CODE]

SELECT [CRS_CODE]
    ,[CRS_TITLE]
    ,[CRS_CREDIT_HOURS]
    ,[CRS_DESCRIPTION]
    ,[CRS_BOOK_NAME]
    ,C.[DPT_CODE]
	,I.[INS_CODE]
    ,[INS_FNAME]
    ,[INS_LNAME]
    ,[INS_START_DATE]
    ,[INS_SALARY]
    ,I.[DPT_CODE]
FROM [dbo].[Course] C INNER JOIN [dbo].[Instructor] I
ON C.[INS_CODE] = I.[INS_CODE]
ORDER BY C.[CRS_CODE]

SELECT [CRS_CODE]
    ,[CRS_TITLE]
    ,[CRS_CREDIT_HOURS]
    ,[CRS_DESCRIPTION]
    ,[CRS_BOOK_NAME]
    ,[INS_CODE]
	,D.[DPT_CODE]
    ,[DPT_NAME]
    ,[DPT_BUDGET]
    ,[DPT_DESCRIPTION]
    ,[DPT_CREATION_DATE]
FROM [dbo].[Course] C INNER JOIN [dbo].[Department] D
ON C.[DPT_CODE] = D.[DPT_CODE]
ORDER BY C.[CRS_CODE]

SELECT [INS_CODE]
    ,[INS_FNAME]
    ,[INS_LNAME]
    ,[INS_START_DATE]
    ,[INS_SALARY]
    ,I.[DPT_CODE]
    ,[DPT_NAME]
    ,[DPT_BUDGET]
    ,[DPT_DESCRIPTION]
    ,[DPT_CREATION_DATE]
FROM [dbo].[Instructor] I INNER JOIN [dbo].[Department] D
ON I.[DPT_CODE] = D.[DPT_CODE]
ORDER BY I.[INS_CODE]

GO


